package com.company;

public class Main {

    public static void main(String[] args) {
        System.out.println("hello world");
        int a = 5;
        int b = 4;
        //Operators
        int sum = a + b;
        int multiply = a*b;
        System.out.println(sum);
        System.out.println(multiply);
        //For decimal values ans. we have to take double or Float
        double A = 5;
        double B = 4;
        double div = A/B;
        double modulo = A%B; // Modulo(%)--> Gives reminder value
        System.out.println(div);
        System.out.println(modulo);
    }
}
